export function getItems(): Promise<ICard[]> {
  return Promise.resolve<ICard[]>(items);
}

export function getItemByName(word: string): Promise<ICard | undefined> {
  return Promise.resolve(
    items.find((it) => it.word.toLowerCase() === word.toLowerCase())
  );
}

export function createItem(item: ICard): Promise<ICard> {
  const isExist =
    typeof items.find(
      (it) => it.word.toLowerCase() === item.word.toLowerCase()
    ) !== "undefined";
  if (isExist) {
    return Promise.reject(
      new Error(`Item with name ${item.word} is already exists.`)
    );
  }
  items.push(item);
  return Promise.resolve(item);
}

export function updateItem(item: ICard): Promise<ICard> {
  const itemIndex = items.findIndex(
    (it) => it.word.toLowerCase() === item.word.toLowerCase()
  );
  if (itemIndex < 0) {
    return Promise.reject(new Error("Item not found"));
  }
  const existsItem = items.splice(itemIndex, 1)[0];
  const newItem: ICard = {
    ...existsItem,
    ...item,
  };
  items.push(newItem);
  return Promise.resolve(newItem);
}

export function deleteItem(word: string): Promise<void> {
  const index = items.findIndex(
    (it) => it.word.toLowerCase() === word.toLowerCase()
  );
  if (index < 0) {
    Promise.reject(new Error("Item not found."));
  }
  items.splice(index, 1);
  return Promise.resolve();
}
